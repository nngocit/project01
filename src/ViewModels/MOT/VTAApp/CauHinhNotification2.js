    ValidateInfoBeforeSubmit() {
        var strErrorMsg = "";
        if (this.AppDevice.DeviceOs == "" || this.AppDevice.DeviceOs === "undefined")
            strErrorMsg += " Device OS phi nhp. <br/>";

        if (this.AppDevice.Alert == null || this.AppDevice.Alert == "" || typeof this.AppDevice.Alert === "undefined")
            strErrorMsg += " Alert phi nhp. <br/>";

        if (this.AppDevice.MessageValue == null || this.AppDevice.MessageValue == "" || typeof this.AppDevice.MessageValue === "undefined")
            strErrorMsg += " MessageValue phi nhp. <br/>";

        if (this.AppDevice.MessageType == null || this.AppDevice.MessageType == "" || typeof this.AppDevice.MessageType === "undefined")
            strErrorMsg += " MessageType phi nhp. <br/>";

        if (this.AppDevice.MessageValue != null && this.AppDevice.MessageValue.length < 3)
            strErrorMsg += " Message Value phi ln hn 3 k t. <br/>";

        if (this.AppDevice.MessageType != null && this.AppDevice.MessageType.length < 3)
            strErrorMsg += " Message Type phi ln hn 3 k t. <br/>";

        console.log("strErrorMsg", strErrorMsg);
        if (strErrorMsg !== "") {
            toastr.error(strErrorMsg, "Li d liu nhp!");
            return false;
        }
        return true;
    }

    ConvertUnicodeToSurrogatePair(unicode) {
        var result = "";
        var s = parseInt(unicode, 16);

        if (s >= 0x10000 && s <= 0x10FFFF) {
            var hi = Math.floor((s - 0x10000) / 0x400) + 0xD800;
            var lo = ((s - 0x10000) % 0x400) + 0xDC00;
            var hi_tem = hi.toString(16).toUpperCase();
            var lo_tem = lo.toString(16).toUpperCase();
            result = "\\u" + hi_tem + "\\u" + lo_tem;
        } else {
            // hi.value = "";
            // lo.value = "";
            result = "";
        }
        return result;
    }

